package comparision_operators;

public class Comparision_operators {
	public static void main(String arg[]) {
        int value1 = 1;
        int value2 = 2;
        if(value1 == value2) {
            System.out.println("value1 == value2");
        }
        if(value1 != value2) {
            System.out.println("value1 != value2");
        }
        if(value1 > value2) {
            System.out.println("value1 > value2");
        }
        if(value1 < value2) {
            System.out.println("value1 < value2");
        }
        if(value1 <= value2) {
            System.out.println("value1 <= value2");
        }
	}

}
