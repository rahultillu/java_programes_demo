package data_conversion;

public class ExplicitDataTypeConversion {
	public static void main(String arg[]) {
		double a= 50.5555555555555;
		System.out.println("double represation:"+a);
		float b= (float)a;
		System.out.println("float represation:"+b);
		long c = (long)b;
		System.out.println("long represation:"+c);
		
	}

}
