package arithimetic_demo;

public class Prepost_demo {
	public static void main(String arg[]) {
		int i = 3;
		i++;
		//print 4
		System.out.println(i);
		++i;
		//print 5;
		System.out.println(i);
		//print 6
		System.out.println(++i);
		//print 6
		System.out.println(i++);
		//print 7
		System.out.println(i);
		
		
	}

}
